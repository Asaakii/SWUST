#include <stdio.h>
int main()
{
	int i,m;
	scanf ("%d",&i);
	if (i<0)
		printf ("error!");
	if (i>=0&&i<=100000)
		m=0.1*i;
	if (i>100000&&i<=200000)
		m=1000+(i-100000)*0.075;
	if (i>200000&&i<=400000)
		m=1750+(i-200000)*0.05;
	if (i>400000&&i<=600000)
		m=2750+(i-400000)*0.03;
	if (i>600000&&i<=1000000)
		m=3350+(i-600000)*0.015;
	if (i>1000000)
		m=3950+(i-1000000)*0.01;
	printf ("%d\n",m);
	return 0;
}
